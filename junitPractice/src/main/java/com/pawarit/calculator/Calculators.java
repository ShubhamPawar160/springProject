package com.pawarit.calculator;

public class Calculators {

	public int sum(int a, int b) {
		int c = a + b;
		return c;
	}

	public int sub(int a, int b) {
		int c = a - b;
		return c;
	}

	public int division(int a, int b) {
		int c = a / b;
		return c;
	}
	
	public int mult(int a,int b)
	{
		int c=a*b;
		return c;
	}

}
